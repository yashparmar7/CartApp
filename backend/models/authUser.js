const mongoose = require("mongoose");

const authUserSchema = new mongoose.Schema(
  {
    userName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const AuthUser = mongoose.model("AuthUser", authUserSchema);

module.exports = AuthUser;
